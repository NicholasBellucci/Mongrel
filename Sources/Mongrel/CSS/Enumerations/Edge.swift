public enum Edge {
    public enum Margin: String {
        case top = "margin-top"
        case right = "margin-right"
        case bottom = "margin-bottom"
        case left = "margin-left"
    }

    public enum Padding: String {
        case top = "padding-top"
        case right = "padding-right"
        case bottom = "padding-bottom"
        case left = "padding-left"
    }
}
